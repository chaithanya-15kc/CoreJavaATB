package Basics_03_16_Dec;

public class lab043 {
    public static void main(String[] args) {
        // Decrement operator.

        int b = 10;
        System.out.println(--b + b--);
        System.out.println(b);

       // --b = b=10, r=9 = 9
        // b-- = b=9, r=8 = 9 = 9+9= 18
       // b=8
    }
}
